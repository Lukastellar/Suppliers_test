    {
        $suppliers = Supplier::all();
        $data = ['suppliers' => []];
        foreach ($suppliers as $supplier) {
            array_push($data['suppliers'], ['id' => $supplier->id, 'name' => $supplier->name] );
        }

        return response()->json($data);
    }

