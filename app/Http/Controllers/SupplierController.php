     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @param  string  $name
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(Request $request, $id, $name)
    {
        $supplier = Supplier::find($id);

        if($supplier){
            $supplier->update(['name' => $name]);
